function downsample(img; maxheight=100)
	h,w = size(img)
	if h <= maxheight
		img
	else
		img[
			floor.(Int,LinRange(1,h,maxheight)),
			floor.(Int,LinRange(1,w,floor(Int, maxheight * w / h)))
		]
	end
end

